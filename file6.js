let a;
a=+prompt("Enter day");
switch (a) {
    case 0:
        {
            console.log("saturday");
            break;
        }

    case 1:
        {
            console.log("sunday");
            break;
        }
    case 2:
        {
            console.log("monday");
            break;
        }
    case 3:
        {
            console.log("tuesday");
            break;
        }
    case 4:
        {
            console.log("wednesday");
            break;
        }
    case 5:
        {
            console.log("thursday");
            break;
        }
    case 6:
        {
            console.log("friday");
            break;
        }
    default:
        console.log("Wrong choice");

}